
import { useState, useEffect } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { authStore } from '@/stores/authStore';

export const useProfileCompletion = (userEmail: string) => {
  const [isChecking, setIsChecking] = useState(true);
  const setProfileComplete = authStore((state) => state.setProfileComplete);

  useEffect(() => {
    const checkProfileCompletion = async () => {
      if (!userEmail) {
        setIsChecking(false);
        return;
      }

      try {
        console.log('üîç Verificando completude do perfil para:', userEmail);
        
        const { data: usuario, error } = await supabase
          .from('usuarios')
          .select('nome, whatsapp')
          .eq('email', userEmail.toLowerCase().trim())
          .single();

        if (error) {
          console.error('‚ùå Erro ao verificar perfil:', error);
          setProfileComplete(false);
        } else if (usuario) {
          // Verificar se tem nome e whatsapp preenchidos
          const isComplete = !!(usuario.nome?.trim() && usuario.whatsapp?.trim());
          console.log('üìã Perfil completo:', isComplete, { nome: usuario.nome, whatsapp: usuario.whatsapp });
          setProfileComplete(isComplete);
        } else {
          console.log('üë§ Usu√°rio n√£o encontrado na tabela usuarios');
          setProfileComplete(false);
        }
      } catch (error) {
        console.error('‚ùå Erro na verifica√ß√£o do perfil:', error);
        setProfileComplete(false);
      } finally {
        setIsChecking(false);
      }
    };

    checkProfileCompletion();
  }, [userEmail, setProfileComplete]);

  return { isChecking };
};
